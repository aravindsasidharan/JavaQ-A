public class Student {


    String name;
    int marks;
    int count;


     public Student(String name, Integer marks,int count) {
        this.name = name;
        this.marks = marks;
        this.count = count;

    }


    @Override
    public String toString() {
        return "Student [name=" + name + ", marks=" + marks + ", count=" + count + "]";
    }

    
    





    
}
